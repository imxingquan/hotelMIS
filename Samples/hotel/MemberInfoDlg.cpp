void CMemberInfoDlg::Refresh()
{
	m_GuiMemInfoList.DeleteAllItems();
	
	hotelMIS::HMemberCtrl hMemberCtrl(m_con.get_con());
	hotelMIS::HMemberVec vec = hMemberCtrl.getAllMember();
	
	hotelMIS::HMemberVec::iterator it;

	int i=0,j;
	CString t;
	m_map.clear();
	
	for ( it = vec.begin(); it != vec.end(); it++)
	{
		j = 1;
		m_map.insert(make_pair(i,it->bh));

		m_GuiMemInfoList.InsertItem(i,it->bh.c_str());				//会员编号
		m_GuiMemInfoList.SetItemText(i,j++,it->mname.c_str());		//姓名
		m_GuiMemInfoList.SetItemText(i,j++,it->sex.c_str());		//性别
		m_GuiMemInfoList.SetItemText(i,j++,it->mbirth.c_str());		//出生日期
		m_GuiMemInfoList.SetItemText(i,j++,it->mdjname.c_str());	//等级名称
		t.Format("%.2f",it->zhekou);
		m_GuiMemInfoList.SetItemText(i,j++,t);						//折扣值
		m_GuiMemInfoList.SetItemText(i,j++,it->telphone.c_str());	//联系电话
		t.Format("%.2f",it->czmoney);
		m_GuiMemInfoList.SetItemText(i,j++,t);						//储值金额
		m_GuiMemInfoList.SetItemText(i,j++,it->rhdate.c_str());		//入会日期

		i++;
	}
}
